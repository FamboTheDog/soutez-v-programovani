package com.company.example;

public class Throw {
    private int number;
    private int length;

    public Throw(int number, int length) {
        this.number = number;
        this.length = length;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
